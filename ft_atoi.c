int	ft_atoi(const char *str)
{
	int		total;
	int		sign;
	char	*s;

	total = 0;
	sign = 1;
	s = (char *)str;
	while (*s == ' ' || (*s >= 9 && *s <= 13))
		s++;
	if (*s == '+' || *s == '-')
	{
		if (*s == '-')
			sign *= -1;
		s++;
	}
	while (*s >= '0' && *s <= '9')
	{
		total *= 10;
		total += *s - '0';
		s++;
	}
	return (total * sign);
}
